    private void BiletListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BiletListMouseClicked
        if (dlm.getSize() > 0) {
            for (int i = 0; i < Labeller.size(); i++) {
                Labeller.get(i).setVisible(true);
            }
            int j = 0;
            for (int i = BiletList.getSelectedIndex() * 8; i < (8 * BiletList.getSelectedIndex()) + 8; i++) {
                Labeller.get(j).setText(LabelBilgi.get(i));
                j++;

            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Satin Aldiginiz Bilet Bulunmamaktadir");
        }
    }//GEN-LAST:event_BiletListMouseClicked

