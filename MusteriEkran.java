    private void musteriBiletler() {
        Musteri m = new Musteri();
        EntityManager em = emf.createEntityManager();
        String maske="";
        String gozluk="";
        for (int i = 0; i < Biletler.size(); i++) {
            Query q = em.createQuery("SELECT b FROM Bilet b where b.id =:paramID");
            q.setParameter("paramID", Integer.parseInt(Biletler.get(i)));
            List<Bilet> bilet = q.getResultList();
            Query q2 = em.createQuery("SELECT f FROM Film f where f.id =:paramFilmId");
            q2.setParameter("paramFilmId", bilet.get(0).getFilmid());
            List<Film> film = q2.getResultList();
            if(bilet.get(0).getN95()==0){maske="Var";}
            if(bilet.get(0).getN95()==1){maske="Yok";}
            if(bilet.get(0).getUcboyut()==0){gozluk="Var";}
            if(bilet.get(0).getUcboyut()==1){gozluk="Yok";}
            dlm.addElement(film.get(0).getAd());
            LabelBilgi.add(film.get(0).getAd());
            LabelBilgi.add(bilet.get(0).getTarih());
            LabelBilgi.add(bilet.get(0).getSaat());
            LabelBilgi.add(Integer.toString(bilet.get(0).getSalonid()));
            LabelBilgi.add(Integer.toString(film.get(0).getFiyat()));
            LabelBilgi.add(Integer.toString(bilet.get(0).getKoltukno()));
            LabelBilgi.add(maske);
            LabelBilgi.add(gozluk);
            LabelBilgi.add(m.getAd());
        }

        BiletList.setModel(dlm);
        em.close();
    }

