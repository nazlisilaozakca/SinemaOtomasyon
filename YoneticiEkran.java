    private void getToplamKazanc(){
        int toplam=0;
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT b FROM Bilet b ");
        List<Bilet> tumBiletler = q.getResultList();
        for (Bilet bilet : tumBiletler) {
            toplam+=bilet.getBiletfiyat();
        }BiletSayisi.setText(Integer.toString(tumBiletler.size()));
        kazanc.setText(Integer.toString(toplam));
        em.close();
    }
    private void Listele() {
        EntityManager em = emf.createEntityManager();

        Query q = em.createQuery("SELECT f FROM Film f ");
        List<Film> tumFilmler = q.getResultList();
        for (Film film : tumFilmler) {
            if (film.getUcboyut()==0) {
                dtm.addRow(new String[]{
                    Integer.toString(film.getId()),
                    film.getAd(),
                    film.getKategori(),
                    Integer.toString(film.getFiyat()),
                    "Var"});
            } else {
                dtm.addRow(new String[]{
                    Integer.toString(film.getId()),
                    film.getAd(),
                    film.getKategori(),
                    Integer.toString(film.getFiyat()),
                    "Yok"});
            }
        }em.close();
    }

