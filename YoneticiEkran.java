    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.rowAtPoint(evt.getPoint());

        int id = Integer.parseInt((String) jTable1.getValueAt(row, 0));
        int n = JOptionPane.showConfirmDialog(
            null,
            "Filmi bu listeden çıkarmak istiyor musunuz?",
            "Emin Misiniz?",
            JOptionPane.YES_NO_OPTION);
        System.out.println(n);
        if (n == 0) {
            EntityManager em = emf.createEntityManager();
            Query q = em.createQuery("delete from Film f WHERE f.id = :parametreID");
            q.setParameter("parametreID", id);
            em.getTransaction().begin();
            q.executeUpdate();
            em.getTransaction().commit();
            em.close();
            JOptionPane.showMessageDialog(null, "Film listeden silindi");

        } else if (n == 1) {
            JOptionPane.showMessageDialog(null, "Film silme işlemi iptal edidi");
        }
    }//GEN-LAST:event_jTable1MouseClicked

