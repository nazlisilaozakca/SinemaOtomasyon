    private void gozlukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gozlukMouseClicked
        if (gozluk.isSelected()) {
            int simdikiFiyat = Integer.parseInt(fiyat.getText());
            fiyat.setText(Integer.toString(simdikiFiyat + 10));
        } else if (!gozluk.isSelected()) {
            int simdikiFiyat = Integer.parseInt(fiyat.getText());
            fiyat.setText(Integer.toString(simdikiFiyat - 10));
        }
    }//GEN-LAST:event_gozlukMouseClicked

    private void maskeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_maskeMouseClicked
        if (maske.isSelected()) {
            int simdikiFiyat = Integer.parseInt(fiyat.getText());
            fiyat.setText(Integer.toString(simdikiFiyat + 50));
        } else if (!maske.isSelected()) {
            int simdikiFiyat = Integer.parseInt(fiyat.getText());
            fiyat.setText(Integer.toString(simdikiFiyat - 50));
        }
    }//GEN-LAST:event_maskeMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (Koltuklar.get(Integer.parseInt(koltukTxt.getText()) - 1).getBackground().equals(Color.red)) {
            JOptionPane.showMessageDialog(
                    null, "Sectiginiz Koltuk Doludur", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            Bilet b = new Bilet();
            b.setFilmid(FilmID);
            b.setId(b.getSonId() + 1);
            b.setKoltukno(Integer.parseInt(koltukTxt.getText()));
            
            b.setN95((short)(maske.isSelected()?0:1));
            b.setUcboyut((short)(gozluk.isSelected()?0:1));
            b.setSaat((String) seansCombo.getSelectedItem());
            b.setTarih((String) tarihCombo.getSelectedItem());
            b.setSalonid(salonCombo.getSelectedIndex());
            b.setBiletfiyat(Integer.parseInt(fiyat.getText()));
            em.persist(b);
            em.getTransaction().commit();
            Query q2 = em.createQuery("SELECT m FROM Musteri m where m.id =:MusteriId");
            q2.setParameter("MusteriId", musteri.getId());
            List<Musteri> Musteri = q2.getResultList();
            Query q = em.createQuery("UPDATE Musteri m SET m.biletid =:BiletParametre WHERE m.id =:MusteriId");
            q.setParameter("MusteriId", musteri.getId());
            q.setParameter("BiletParametre", Musteri.get(0).getBiletid() + Integer.toString(b.getSonId()) + " ");
            em.getTransaction().begin();
            q.executeUpdate();
            em.getTransaction().commit();
            em.close();
                this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

