    private String getFilmAd() {
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT f FROM Film f ");
        List<Film> tumFilmler = q.getResultList();
        for (Film film : tumFilmler) {
            if (film.getId() == FilmID) {
                em.close();
                return film.getAd();
            }
        }
        em.close();
        return "";
    }

    private int getFilmFiyat() {
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT f FROM Film f ");
        List<Film> tumFilmler = q.getResultList();
        for (Film film : tumFilmler) {
            if (film.getId() == FilmID) {
                em.close();
                return film.getFiyat();
            }
        }
        em.close();
        return 0;
    }

    private void KoltuklariGoster() {
        Ekran.setVisible(true);
        Ekran.setOpaque(true);
        Ekran.setBackground(Color.GRAY);
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT b FROM Bilet b where b.filmid =:paramFilmID");
        q.setParameter("paramFilmID", FilmID);
        List<Bilet> tumBiletler = q.getResultList();
        
        for (JLabel jLabel : Koltuklar) {
            jLabel.setVisible(true);
            jLabel.setOpaque(true);
        }
        
        for (int i = 0; i < Koltuklar.size(); i++) {
            if (i % 2 == 1 && (i < 6 || (i > 11 && i < 18))) {
                Koltuklar.get(i).setBackground(Color.red);
            } else if (i % 2 == 0 && ((i > 4 && i < 11) || (i > 16 && i < 23))) {
                Koltuklar.get(i).setBackground(Color.red);
            } else {
                Koltuklar.get(i).setBackground(Color.green);
            }

        }for (Bilet bilet : tumBiletler) {
            if (bilet.getSalonid() == salonCombo.getSelectedIndex() && bilet.getTarih().equals((String) tarihCombo.getSelectedItem()) && bilet.getSaat().equals((String) seansCombo.getSelectedItem())) {
                Koltuklar.get(bilet.getKoltukno() - 1).setBackground(Color.red);
            }
        }
        
        em.close();
    }

